public class Switchsample {
    public static void main(String[] args) {

        String name="ramu";
        
        switch(name){
            case "ramu":
            System.out.println("sunday");
            break;
            case "raju":
            System.out.println("monday");
            break;
            case "steven":
            System.out.println("tuesday");
            break;
            default:
            System.out.println("invalid data");
        }

    }
}
