// Convert an int to a double using implicit casting.

public class varaible_declaration {
    public static void main(String[] args) {
        int num = 50;        // Integer value
        double converted = num;  // Implicit conversion to double
        
        System.out.println("Integer Value: " + num);
        System.out.println("Converted to Double: " + converted);
    }
}


